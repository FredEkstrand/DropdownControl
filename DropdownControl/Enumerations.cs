using System;
using System.Text;


namespace Ekstrand.Windows.Forms
{
    /// <summary>
    /// Specifies the docking side of the Dropdown Controls Pop-up window.
    /// </summary>
    public enum DockSide
    {
        /// <summary>
        /// Left side of the Dropdown Control.
        /// </summary>
        Left,
        /// <summary>
        /// Right side of the Dropdown Control.
        /// </summary>
        Right
    }

    /// <summary>
    /// Specifies the side to render the button on the Dropdown Control.
    /// </summary>
    public enum DropdownButtonSide
    {
        /// <summary>
        /// Left side of the control.
        /// </summary>
        Left = 1,
        /// <summary>
        /// Right side of the control.
        /// </summary>
        Right = 0
    }
}